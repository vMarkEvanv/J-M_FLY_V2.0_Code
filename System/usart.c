int CNT=0;
void CRC_INIT(){
	unsigned char crc;
	unsigned char crc_temp;
	int shift = 0;
	for(int idx=0;idx<256;++idx){
		crc = idx;
		while(shift < 8){
			shift += 1;
			if(crc & 0x80){
				crc_temp = poly;
			}
			else{
				crc_temp = 0;
			}
		}
    crc = (crc << 1) ^ crc_temp;
    lut[idx] = crc & 0xff;
    idx += 1;
	}
}


